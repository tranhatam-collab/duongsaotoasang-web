function normalizeLang(value) {
  return value === "en" ? "en" : "vi";
}

function parseLimit(value, fallback = 24, max = 100) {
  const n = Number.parseInt(value, 10);
  if (!Number.isFinite(n) || n <= 0) return fallback;
  return Math.min(n, max);
}

function normalizeText(value) {
  return String(value || "")
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "");
}

function pickLocalizedFields(item, lang) {
  const next = { ...item };
  next.title = lang === "en"
    ? (item.title_en || item.title || item.title_vi || "")
    : (item.title_vi || item.title || item.title_en || "");

  next.excerpt = lang === "en"
    ? (item.excerpt_en || item.excerpt || item.excerpt_vi || "")
    : (item.excerpt_vi || item.excerpt || item.excerpt_en || "");

  next.content = lang === "en"
    ? (item.content_en || item.content || item.content_vi || "")
    : (item.content_vi || item.content || item.content_en || "");

  return next;
}

function filterFallbackData(items, { q = "", type = "", limit = 24, lang = "vi" } = {}) {
  let result = [...items];

  if (type) {
    result = result.filter((item) => String(item.type || "") === String(type));
  }

  if (q) {
    const keyword = normalizeText(q);
    result = result.filter((item) => {
      const haystack = normalizeText([
        item.slug,
        item.title_vi,
        item.title_en,
        item.excerpt_vi,
        item.excerpt_en,
        item.content_vi,
        item.content_en,
        item.tags
      ].join(" "));
      return haystack.includes(keyword);
    });
  }

  result.sort((a, b) => {
    const da = new Date(a.created_at || 0).getTime();
    const db = new Date(b.created_at || 0).getTime();
    return db - da;
  });

  return result
    .slice(0, limit)
    .map((item) => pickLocalizedFields(item, lang));
}

function json(data, status = 200) {
  return new Response(JSON.stringify(data), {
    status,
    headers: {
      "Content-Type": "application/json; charset=utf-8",
      "Cache-Control": "public, max-age=300"
    }
  });
}

export async function onRequest(context) {
  const { request, env } = context;
  const url = new URL(request.url);

  const lang = normalizeLang(url.searchParams.get("lang"));
  const limit = parseLimit(url.searchParams.get("limit"), 24, 100);
  const type = (url.searchParams.get("type") || "").trim();
  const q = (url.searchParams.get("q") || "").trim();

  if (!env?.DB) {
    const fallback = filterFallbackData(FALLBACK_CONTENTS, { q, type, limit, lang });
    return json(fallback);
  }

  try {
    let query = `
      SELECT
        id,
        slug,
        type,
        title,
        title_vi,
        title_en,
        excerpt,
        excerpt_vi,
        excerpt_en,
        content,
        content_vi,
        content_en,
        cover,
        cover_url,
        tags,
        published,
        created_at,
        updated_at
      FROM contents
      WHERE COALESCE(published, 1) = 1
    `;

    const bindings = [];

    if (type) {
      query += ` AND type = ?`;
      bindings.push(type);
    }

    if (q) {
      query += `
        AND (
          slug LIKE ?
          OR title LIKE ?
          OR title_vi LIKE ?
          OR title_en LIKE ?
          OR excerpt LIKE ?
          OR excerpt_vi LIKE ?
          OR excerpt_en LIKE ?
          OR content LIKE ?
          OR content_vi LIKE ?
          OR content_en LIKE ?
          OR tags LIKE ?
        )
      `;
      const like = `%${q}%`;
      bindings.push(
        like, like, like, like, like, like, like, like, like, like, like
      );
    }

    query += ` ORDER BY datetime(created_at) DESC LIMIT ?`;
    bindings.push(limit);

    const stmt = env.DB.prepare(query).bind(...bindings);
    const { results } = await stmt.all();

    if (Array.isArray(results) && results.length) {
      const data = results.map((item) => pickLocalizedFields(item, lang));
      return json(data);
    }

    const fallback = filterFallbackData(FALLBACK_CONTENTS, { q, type, limit, lang });
    return json(fallback);
  } catch (error) {
    const fallback = filterFallbackData(FALLBACK_CONTENTS, { q, type, limit, lang });
    return json(fallback);
  }
}
