function normalizeLang(value) {
  return value === "en" ? "en" : "vi";
}

function parseLimit(value, fallback = 20, max = 50) {
  const n = Number.parseInt(value, 10);
  if (!Number.isFinite(n) || n <= 0) return fallback;
  return Math.min(n, max);
}

function normalizeText(value) {
  return String(value || "")
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "");
}

function stripHtml(value) {
  return String(value || "")
    .replace(/<style[\s\S]*?<\/style>/gi, " ")
    .replace(/<script[\s\S]*?<\/script>/gi, " ")
    .replace(/<[^>]+>/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function pickLocalizedFields(item, lang) {
  return {
    id: item.id,
    slug: item.slug,
    type: item.type || "post",
    title: lang === "en"
      ? (item.title_en || item.title || item.title_vi || "")
      : (item.title_vi || item.title || item.title_en || ""),
    title_vi: item.title_vi || item.title || "",
    title_en: item.title_en || item.title || "",
    excerpt: lang === "en"
      ? (item.excerpt_en || item.excerpt || item.excerpt_vi || "")
      : (item.excerpt_vi || item.excerpt || item.excerpt_en || ""),
    excerpt_vi: item.excerpt_vi || item.excerpt || "",
    excerpt_en: item.excerpt_en || item.excerpt || "",
    cover: item.cover || "",
    cover_url: item.cover_url || item.cover || "",
    tags: item.tags || "",
    created_at: item.created_at || "",
    updated_at: item.updated_at || ""
  };
}

function searchFallback(items, { q, limit, lang }) {
  const keyword = normalizeText(q);

  const results = items
    .filter(item => Number(item.published ?? 1) === 1)
    .filter(item => {
      const haystack = normalizeText([
        item.slug,
        item.type,
        item.title,
        item.title_vi,
        item.title_en,
        item.excerpt,
        item.excerpt_vi,
        item.excerpt_en,
        stripHtml(item.content),
        stripHtml(item.content_vi),
        stripHtml(item.content_en),
        item.tags
      ].join(" "));

      return haystack.includes(keyword);
    })
    .sort((a, b) => {
      const da = new Date(a.created_at || 0).getTime();
      const db = new Date(b.created_at || 0).getTime();
      return db - da;
    })
    .slice(0, limit)
    .map(item => pickLocalizedFields(item, lang));

  return results;
}

function json(data, status = 200) {
  return new Response(JSON.stringify(data), {
    status,
    headers: {
      "Content-Type": "application/json; charset=utf-8",
      "Cache-Control": "public, max-age=180"
    }
  });
}

export async function onRequest(context) {
  const { request, env } = context;
  const url = new URL(request.url);

  const q = (url.searchParams.get("q") || "").trim();
  const lang = normalizeLang(url.searchParams.get("lang"));
  const limit = parseLimit(url.searchParams.get("limit"), 20, 50);

  if (!q) {
    return json([]);
  }

  if (!env?.DB) {
    return json(searchFallback(FALLBACK_CONTENTS, { q, limit, lang }));
  }

  try {
    const like = `%${q}%`;

    const query = `
      SELECT
        id,
        slug,
        type,
        title,
        title_vi,
        title_en,
        excerpt,
        excerpt_vi,
        excerpt_en,
        content,
        content_vi,
        content_en,
        cover,
        cover_url,
        tags,
        published,
        created_at,
        updated_at
      FROM contents
      WHERE COALESCE(published, 1) = 1
        AND (
          slug LIKE ?
          OR type LIKE ?
          OR title LIKE ?
          OR title_vi LIKE ?
          OR title_en LIKE ?
          OR excerpt LIKE ?
          OR excerpt_vi LIKE ?
          OR excerpt_en LIKE ?
          OR content LIKE ?
          OR content_vi LIKE ?
          OR content_en LIKE ?
          OR tags LIKE ?
        )
      ORDER BY datetime(created_at) DESC
      LIMIT ?
    `;

    const stmt = env.DB.prepare(query).bind(
      like,
      like,
      like,
      like,
      like,
      like,
      like,
      like,
      like,
      like,
      like,
      like,
      limit
    );

    const { results } = await stmt.all();

    if (Array.isArray(results) && results.length) {
      return json(results.map(item => pickLocalizedFields(item, lang)));
    }

    return json(searchFallback(FALLBACK_CONTENTS, { q, limit, lang }));
  } catch (error) {
    return json(searchFallback(FALLBACK_CONTENTS, { q, limit, lang }));
  }
}
